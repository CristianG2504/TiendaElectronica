private void abrirProveedores() throws SQLException {
        jPanel2.removeAll();
        jPanel2.setLayout(new java.awt.BorderLayout());

        // Crear vista y DAO
        ProveedorView vistaProveedor = new ProveedorView();
        ProveedorDaoImpl dao = new ProveedorDaoImpl();

        // Crear controlador
        Controller.ProveedorController controller
                = new Controller.ProveedorController(vistaProveedor, dao);

        jPanel2.add(vistaProveedor, java.awt.BorderLayout.CENTER);
        jPanel2.revalidate();
        jPanel2.repaint();
    }

